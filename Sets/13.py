# Write a Python program to find the length of a set.

mySet1 = {5, 10, 3, 15, 2, 20}

lenSet = len(mySet1)

print(lenSet) # 6

# Create a set with repeated elements, all 5.
mySet2 = {5, 5, 5, 5, 5, 5}

lenSet = len(mySet2)

print(lenSet) # 1

# Create a set with repeated elements, all 5 and an additional element 7.
mySet3 = {5, 5, 5, 5, 5, 5, 7}

lenSet = len(mySet3)

print(lenSet) # 2