# Write a Python program to check if a given value is present in a set or not.

nums = {1, 3, 5, 7, 9, 11}

print(6 in nums) # False

print(7 in nums) # True

print(11 in nums) # True

print(0 in nums) # False

print(6 not in nums) # True

print(7 not in nums) # False

print(11 not in nums) # False

print(0 not in nums) # True